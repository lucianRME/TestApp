package calc;

public class Calculator 
{
    public int aduna(int a, int b)
    {
        return a + b;
    }

    public int scade(int a, int b)
    {
        return a - b;
    }

    public int inmulteste(int a, int b)
    {
        return a * b;
    }

    public float imparte(int a, int b)
    {
        return a/b;
    }
}
